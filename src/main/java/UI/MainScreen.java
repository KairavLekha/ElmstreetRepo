    private void populateStore(ArrayList<Item> items, MouseListener l, Item featItem) {
        featItemLabel.setText(featItem.getName());
        originalPriceLabel.setText("Was R" + featItem.getSellingPrice());
        stockLabel.setText("Only " + featItem.getQuantity() + " units remaining");
        imageFeatured.setIcon(new ImageIcon(featItem.getImagePath() + ".jpg"));
        featItemPriceLabel.setText("Now R" + featItem.getCurrentPrice());
        discountLabel.setText("Save " + featItem.getDiscount() + "%");
        resetStore(l);
        int lastItemIndex = items.size() - 1;
        for (int i = 0; i <= lastItemIndex; i++) {
            Item currentItem = items.get(i);
            itemNames.get(i).setText(currentItem.getName());
            itemPrices.get(i).setText("Price: R" + currentItem.getCurrentPrice());
            itemImages.get(i).setIcon(new ImageIcon(currentItem.getImagePath() + "small.jpg"));
            itemImages.get(i).addMouseListener(l);
        }
    }

    private void bulidUI(User inU) {

// Get user data using the getters
        String fullname = u.getFullname();
        String phoneNumber = u.getPhoneNumber();
        String address = u.getAddress();
        String email = u.getEmail();
        boolean isOwner = u.isOwner();

// Populate text fields with user data
        fNameTextField.setText(fullname.split(" ")[0]); // First Name
        sNameTextField.setText(fullname.split(" ")[1]); // Last Name
        numberTextField.setText(phoneNumber); // Contact Number
        emailTextField.setText(email); // Email
        addressTextField.setText(address); // Address
        PasswordField.setText(u.getPassword());
        PasswordField1.setText(u.getPassword());

// Set the correct radio button based on 'isOwner' value
        if (isOwner) {
            ownerButton.setSelected(true);
        } else {
            customerButton.setSelected(true);
        }

        if (inU.isOwner()) {
            orderPanel.setName("Orders");
        } else {
            orderPanel.setName("Order History");
            idRadioButton.setEnabled(false);

        }
    }

